.386 

STCK SEGMENT STACK 'STACK'
	DB 100 DUP(0)
STCK ENDS

DSEG SEGMENT PARA PUBLIC 'DATA' USE16
		STRING	DB 'Iniciando no Desenvolvimento da FAT 32!!'
DSEG ENDS

CSEG SEGMENT PUBLIC 'CODE' USE16
	ASSUME CS:CSEG, DS:DSEG

	START PROC NEAR
	
		MOV		AX, SEG DSEG
		MOV		DS, AX
		MOV		ES, AX
		
		MOV		AX, 03h
		INT		10h
	
		MOV		SI, OFFSET STRING
		MOV		CX, LENGTHOF STRING
		CALL	PRINT
		
		JMP		$
		
PRINT:
		LODSB
		MOV		AH, 0Eh
		INT		10h
		LOOP 	PRINT
		
		RET

	START ENDP
	
CSEG ENDS

END START