.MODEL SMALL
.STACK
.DATA 
	COLORTEXT DB "WE LOVE DOGS!!!$"
.CODE
START:
MOV AX, @DATA
MOV ES, AX
MOV BP, OFFSET COLORTEXT ; ES: BP POINTS TO THE TEXT
MOV AH, 13H ; WRITE THE STRING
MOV AL, 01H; ATTRIBUTE IN BL, MOVE CURSOR TO THAT POSITION
XOR BH,BH ; VIDEO PAGE = 0
MOV BL, 2 ;GREEN
MOV CX, 15 ; LENGTH OF THE STRING
MOV DH, 8 ;ROW TO PLACE STRING
MOV DL, 8 ; COLUMN TO PLACE STRING
INT 10H

MOV AX, 4C00H
INT 21H

END START