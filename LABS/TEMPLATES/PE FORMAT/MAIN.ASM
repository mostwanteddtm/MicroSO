.286
.MODEL SMALL

INCLUDE BASE.INC

PUBLIC MAIN_CSEG															
EXTRN PRINTF:FAR

SSEG SEGMENT STACK 'STACK'
	DW 0100h
SSEG ENDS

DSEG SEGMENT PUBLIC 'DATA' USE16
	COPYRIGHT	DB	'Copyright Marcos Roberto da Costa', 0
	MESSAGE 	DB 	'MODELO PADRAO DE PORTABLE EXECUTABLE FILE FORMAT!!', 0
	CRLF		DB 	CR, LF, 0 
	MAIN_CSEG	DW	CSEG					; ASSIM CONSIGO CRIAR UMA PONTE PARA ARQUIVOS EXTERNOS ACESSAREM O SEGMENTO CSEG	
DSEG ENDS

CSEG SEGMENT PUBLIC 'CODE' USE16
	ASSUME CS:CSEG,DS:DSEG,ES:DSEG,SS:SSEG

	START PROC 
		
		MOV		AX, DSEG
		MOV		DS, AX
		MOV 	ES, AX

		MOV		SI, OFFSET COPYRIGHT
		CALL	PRINTF
		
		MOV		SI, OFFSET CRLF
		CALL	PRINTF
		
		MOV		SI, OFFSET MESSAGE
		CALL	PRINTF
		
		MOV		AH, 4Ch
		INT		21h
	
	START ENDP
	
CSEG ENDS

END START