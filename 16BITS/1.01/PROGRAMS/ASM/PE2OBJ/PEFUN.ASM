PUBLIC PRINT_MESSAGE

DT_SEG SEGMENT PARA PUBLIC 'DATA' USE16

	MESSAGE DB	"16 Bits two objects portable executable file format!!", 0

DT_SEG ENDS

FN_SEG SEGMENT PARA PUBLIC 'CODE' USE16
	ASSUME CS:FN_SEG
	
	PRINT_MESSAGE PROC FAR
	
		MOV		AX, DT_SEG
		MOV		DS, AX
		MOV		ES, AX
		
		MOV		AH, 0Eh
		MOV		SI, OFFSET MESSAGE
		
	CPRINT:
		LODSB
		CMP		AL, 0
		JE		FPRINT
		INT		10h
		JMP		CPRINT
		
	FPRINT:
		IRET
		
	PRINT_MESSAGE ENDP

FN_SEG ENDS

END PRINT_MESSAGE